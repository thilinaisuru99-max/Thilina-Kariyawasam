// ---------------- LIGHTBOX ----------------
const lightbox = document.getElementById('lightbox');
const lbImage = document.getElementById('lbImage');
const lbTitle = document.getElementById('lbTitle');
const lbCount = document.getElementById('lbCount');
const lbThumbs = document.getElementById('lbThumbs');
let currentProject = null;
let currentIndex = 0;

function openLightbox(projectId){
  currentProject = projects.find(p => p.id === projectId);
  currentIndex = 0;
  lbTitle.innerHTML = currentProject.title;
  renderLightbox();
  lightbox.classList.add('open');
  document.body.style.overflow = 'hidden';
}

function renderLightbox(){
  const imgs = currentProject.images;
  lbImage.src = imgs[currentIndex];
  lbImage.alt = currentProject.title + ' — view ' + (currentIndex+1);
  lbImage.style.animation = 'none';
  void lbImage.offsetWidth;
  lbImage.style.animation = '';
  lbCount.textContent = pad(currentIndex+1) + ' / ' + pad(imgs.length);

  lbThumbs.innerHTML = '';
  imgs.forEach((src, i) => {
    const t = document.createElement('img');
    t.src = src;
    t.className = i === currentIndex ? 'active' : '';
    t.addEventListener('click', () => { currentIndex = i; renderLightbox(); });
    lbThumbs.appendChild(t);
  });
}

function closeLightbox(){
  lightbox.classList.remove('open');
  document.body.style.overflow = '';
}

function nextImage(){
  currentIndex = (currentIndex + 1) % currentProject.images.length;
  renderLightbox();
}
function prevImage(){
  currentIndex = (currentIndex - 1 + currentProject.images.length) % currentProject.images.length;
  renderLightbox();
}

document.getElementById('lbClose').addEventListener('click', closeLightbox);
document.getElementById('lbNext').addEventListener('click', nextImage);
document.getElementById('lbPrev').addEventListener('click', prevImage);
lightbox.addEventListener('click', (e) => { if (e.target === lightbox) closeLightbox(); });

document.addEventListener('keydown', (e) => {
  if (!lightbox.classList.contains('open')) return;
  if (e.key === 'Escape') closeLightbox();
  if (e.key === 'ArrowRight') nextImage();
  if (e.key === 'ArrowLeft') prevImage();
});
