// ---------------- BUILD GRID ----------------
const grid = document.getElementById('grid');
const grid2 = document.getElementById('grid2');

function pad(n){ return String(n).padStart(2,'0'); }

function buildCard(p, idx){
  const card = document.createElement('div');
  card.className = 'card' + (p.wide ? ' wide' : '');
  card.dataset.category = p.category;
  card.dataset.id = p.id;
  card.innerHTML = `
    <img src="${p.cover}" alt="${p.title}" loading="lazy">
    <div class="card-overlay">
      <div class="card-top">
        <span class="card-fig mono">FIG. ${pad(p.id)}</span>
        <span class="card-count mono">${p.images.length} views</span>
      </div>
      <div class="card-bottom">
        <h3 class="serif">${p.title}</h3>
        <div class="tag">${p.tag}</div>
      </div>
    </div>
  `;
  card.addEventListener('click', () => openLightbox(p.id));
  return card;
}

projects.forEach((p, i) => {
  const card = buildCard(p, i);
  if (i < 6) grid.appendChild(card); else grid2.appendChild(card);
});

// ---------------- FILTERS ----------------
const filterBtns = document.querySelectorAll('.filters button');
filterBtns.forEach(btn => {
  btn.addEventListener('click', () => {
    filterBtns.forEach(b => b.classList.remove('active'));
    btn.classList.add('active');
    const f = btn.dataset.filter;
    document.querySelectorAll('.card').forEach(c => {
      const show = (f === 'all' || c.dataset.category === f);
      c.classList.toggle('hidden', !show);
    });
  });
});

// ---------------- REVEAL ON SCROLL ----------------
const revealTargets = document.querySelectorAll('.reveal, .card');
const io = new IntersectionObserver((entries) => {
  entries.forEach(e => {
    if (e.isIntersecting){
      e.target.classList.add('in');
      io.unobserve(e.target);
    }
  });
}, { threshold: 0.12 });
revealTargets.forEach(t => io.observe(t));

